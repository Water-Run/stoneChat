<?php
/* -------------------------------------------------------------------------
 * stoneChat / tests/smoke/lib.php
 *
 * Common helpers for the smoke test suite. Sourced from every
 * test_*.php and from run.php.
 *
 *   sc_test_init()              one-time setup (paths, defaults)
 *   sc_test_swap_conf()         back up the real CONF.ini and install
 *                               a test-friendly one with a real
 *                               password and a single mock provider
 *   sc_test_restore_conf()      undo sc_test_swap_conf()
 *   sc_test_start_server()      spawn `php -S` against Pages/router.php
 *   sc_test_stop_server()       kill the spawned php -S
 *   sc_test_wait_server()       poll GET / until a 2xx/3xx comes back
 *
 *   sc_test_http($method, $path, $body) -- issues the request via the
 *                               PHP curl extension, returns an
 *                               associative array with 'status',
 *                               'body', and 'headers'. The cookie jar
 *                               at $GLOBALS['SC_COOKIE_JAR'] is read
 *                               and written automatically.
 *
 *   sc_test_assert_eq($expected, $actual, $msg)
 *   sc_test_assert_status($expected, $msg)
 *   sc_test_assert_json_field($field, $expected, $msg)
 *   sc_test_extract_json($field) -- read a top-level string field
 *
 *   sc_test_login($password)    posts the login form; stores the
 *                               resulting sc_auth cookie so the
 *                               cookie jar sends it on every
 *                               subsequent request.
 *   sc_test_logout()            posts the logout form; clears the
 *                               stored cookie.
 *
 *   sc_test_report()            print the per-process pass/fail tally
 *
 * Counts live in $GLOBALS['SC_TEST_PASS'] / $GLOBALS['SC_TEST_FAIL'].
 *
 * PHP 5.2 compatible.
 * ------------------------------------------------------------------------- */


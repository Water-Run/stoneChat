        $ca   = $GLOBALS['SC_CA_CERT'];
        $stun = $GLOBALS['SC_PHP_BIN'];
        $port = (int)$GLOBALS['SC_PORT'];
        $pw   = $GLOBALS['SC_TEST_PASSWORD'];
        $body = <<<INI
